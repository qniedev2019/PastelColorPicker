        private void timer1_Tick(object sender, EventArgs e)
        {
            int fadingSpeed = 15;
            lblNoti.ForeColor = Color.FromArgb(lblNoti.ForeColor.R + fadingSpeed, lblNoti.ForeColor.G + fadingSpeed, lblNoti.ForeColor.B + fadingSpeed);

            if (lblNoti.ForeColor.R >= this.BackColor.R)
            {
                timer1.Stop();
                lblNoti.ForeColor = this.BackColor;
                lblNoti.Visible = false;
                lblNoti.ForeColor = Color.Black;
            }



        }

